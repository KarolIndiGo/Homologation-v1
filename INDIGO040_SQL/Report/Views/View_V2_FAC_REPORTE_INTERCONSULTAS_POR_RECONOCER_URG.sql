--SE CREA EL CTE DE JUSTIFICACIONES, PARA QUE ME RETORNE TODOS LOS ORDENAMIENTOS CON ALGUNA JUSTIFIACION.
CTE_RECONOCIMIENTOS_INTERCONSULTA AS
(
SELECT ING.NUMINGRES,ING.NUMEFOLIO,ing.IESTADOIN,ing.[FECHA INGRESO] ,ING.ENTIDAD ,ING.[GRUPO ATENCION] ,ING.IPCODPACI ,ING.IPNOMCOMP ,ING.CODSERIPS ,ING.DESSERIPS,ING.[CANTIDAD ORDENADA]  ,ING.CANSERIPS,CAM.UFUDESCRI ,
CAM.NUMCAMHOS,EGR.FECEGRESO,CAR.CUPS 'CUPS CARGADO' ,CAR .CANTIDAD 'CANTIDAD CARGADA' ,ING.[FECHA REALIZADO MINIMA] ,ING.[FECHA REALIZADO MAXIMA] 
FROM CTE_DATOS_INTERCONSULTAS  ING
LEFT JOIN CTE_ULTIMA_CAMAS_OCUPADA AS CAM ON ING.NUMINGRES =CAM.NUMINGRES
LEFT JOIN CTE_EGRESO_CAMA EGR ON EGR.NUMINGRES =ING.NUMINGRES
LEFT JOIN CTE_ORDENES_CON_INTER_CARGADAS CAR ON CAR.AdmissionNumber =ING.NUMINGRES AND CAR.CUPS =ING.CODSERIPS
WHERE ING.[CANTIDAD ORDENADA] <>ISNULL(CAR.CANTIDAD,0)  AND ING.[CANTIDAD ORDENADA] >ISNULL(CAR.CANTIDAD,0)
--AND CAM.NUMCAMHOS LIKE 'U%' OR CAM.NUMCAMHOS LIKE 'O%' OR CAM.NUMCAMHOS LIKE 'SRE%'
),

CTE_JUSTIFICACIONES AS
(
SELECT DISTINCT IM.NUMINGRES,IM.NUMEFOLIO,IM.CODSERIPS,JUS.Code
FROM 
[Billing].[BillingJustificationControl] JUS INNER JOIN
[Billing].[AccountControlJustification] CON ON JUS.ID=JustificationId INNER JOIN
dbo.HCORDINTE IM ON CON.EntityId=IM.AUTO AND CON.EntityName='HCORDINTE'
)
-- SE CREA LA NUEVA CONSULTA UNIENDO EL CTE CON EL CTE_JUSTIFICACIONES PARA QUE NO ME MUESTRE LAS ORDENES QUE SE ENCUENTRAN JUSTIFICADAS
SELECT INTE.* 
FROM 
CTE_RECONOCIMIENTOS_INTERCONSULTA INTE LEFT JOIN
CTE_JUSTIFICACIONES JUS ON INTE.NUMINGRES=JUS.NUMINGRES AND INTE.NUMEFOLIO=JUS.NUMEFOLIO AND INTE.CODSERIPS=JUS.CODSERIPS
WHERE JUS.Code IS NULL AND INTE.UFUDESCRI in ('PEDIATRIA URGENCIAS','URGENCIAS ADULTOS','PABELLON URGENCIAS ADULTOS','GINECOBSTETRICIA URGENCIAS')
