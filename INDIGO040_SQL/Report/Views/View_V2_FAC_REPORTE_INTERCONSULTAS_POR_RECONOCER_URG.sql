          FROM dbo.ADINGRESO ing with (nolock)
	      INNER JOIN dbo.HCORDINTE A with (nolock) ON ing.NUMINGRES = A.NUMINGRES
		  INNER JOIN DBO.INUNIFUNC FUN ON FUN.UFUCODIGO =ING.UFUCODIGO
	      INNER JOIN dbo.INCUPSIPS B with (nolock) ON A.CODSERIPS=B.CODSERIPS
		  INNER JOIN DBO.INPACIENT AS P with (nolock) ON P.IPCODPACI =A.IPCODPACI
		  INNER JOIN CTE_INTERCONSULTAS_UNICOS AS UNI with (nolock) ON UNI.NUMINGRES =ING.NUMINGRES
		  WHERE A.ESTSERIPS ='3' and A.MANEXTPRO ='0' AND FUN.UFUTIPUNI ='1'--ING.UFUCODIGO in ('1111001','1113008','1113009')
		  GROUP BY ING.NUMINGRES,A.IPCODPACI,P.IPNOMCOMP ,A.CODSERIPS,B.DESSERIPS,UNI.IESTADOIN
	    ) AS G ON G.NUMINGRES =ING.NUMINGRES AND G.CODSERIPS =A.CODSERIPS
		LEFT JOIN DBO.HCHISPACA AS HIS ON HIS.NUMINGRES =A.NUMINGRES AND HIS.NUMEFOLIO =A.NUMEFOLIO
	    WHERE A.ESTSERIPS ='3' and A.MANEXTPRO ='0' AND A.GENSERVICEORDER IS NULL AND FUN.UFUTIPUNI =1--ING.UFUCODIGO in ('1111001','1113008','1113009')
	    GROUP BY ING.NUMINGRES,A.NUMEFOLIO,A.IPCODPACI,P.IPNOMCOMP ,A.CODSERIPS,B.DESSERIPS,G.IESTADOIN,G.[CANTIDAD ORDENADA],HA.Name , CG.Name ,ING.IFECHAING,FUN.UFUDESCRI ,FUN.UFUTIPUNI 
	 UNION ALL
      SELECT ING.NUMINGRES,A.NUMEFOLIO,A.IPCODPACI,P.IPNOMCOMP ,RTRIM(A.CODSERIPS) AS CODSERIPS,B.DESSERIPS,SUM(A.CANSERIPS ) AS CANSERIPS,G.IESTADOIN,G.[CANTIDAD ORDENADA],
	  HA.Name ENTIDAD, CG.Name 'GRUPO ATENCION',CAST(ING.IFECHAING AS DATE) 'FECHA INGRESO',FUN.UFUDESCRI ,FUN.UFUTIPUNI,
