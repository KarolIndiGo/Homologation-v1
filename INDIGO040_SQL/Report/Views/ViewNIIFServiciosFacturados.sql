	 WHEN HA.EntityType IS NULL AND F.DOCUMENTTYPE LIKE '6' THEN 'N/A' END AS [TIPO REGIMEN],
CASE WHEN ING.TIPOINGRE=1 THEN 'AMBULATORIO' 
	 WHEN ING.TIPOINGRE=2 THEN 'HOSPITALARIO' 
