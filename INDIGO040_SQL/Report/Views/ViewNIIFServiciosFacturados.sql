  INNER JOIN Billing.ServiceOrderDetail AS SOD ON SO.Id =SOD.ServiceOrderId 
  INNER JOIN CTE_FACTURA_DETALLE AS ORDF ON SOD.PackageServiceOrderDetailId =ORDF.ID_ORDE_DETALLE 
