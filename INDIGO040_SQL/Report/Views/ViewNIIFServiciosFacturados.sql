  LEFT JOIN Contract.CUPSEntity AS CUPS_PAQ ON CUPS_PAQ.Id = SOD_PAQ.CUPSEntityId
  LEFT JOIN Inventory.InventoryProduct AS IPR ON IPR.Id = SOD.ProductId
